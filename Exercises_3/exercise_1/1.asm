IN 10H
	
	MVI A,10H  ;set display mode
	STA 0B00H 
	STA 0B01H 
	STA 0B02H 
	STA 0B03H 
	STA 0B04H 
	STA 0B05H	
	
	MVI A,0DH 		
	SIM ; set interrupt mode
	EI ; enable interrupt
LOOP:
	JMP LOOP
INTR_ROUTINE:
	POP H			
	EI				
	MVI A,00H 		
	STA 3000H
	MVI H,05H 		
	MOV A,H
	DCR A			
	STA 0B05H			
SECONDS:
	MVI A,05H			
LEDS_ON_ON:
	STA 0B04H			
	CALL DISPLAY		
	DCR A 			
	CPI 00H 			
	JNZ LEDS_ON_ON	 	
	CALL ZERO			
	DCR H			
	JZ EXIT			
	MOV A,H
	DCR A		
	STA 0B05H
	JMP SECONDS		
EXIT:
	MVI A,FFH 		
	STA 3000H 
	JMP LOOP			
DISPLAY: 	
	LXI B,0064H 		
	LXI D,0B00H		
	PUSH PSW
	PUSH H
	PUSH D
	PUSH B
	CALL STDM
	MVI A,0AH 		
ONE_SECOND:
	CALL DCD			
	CALL DELB
	DCR A
	CPI 00H
	JNZ ONE_SECOND
	POP B
	POP D
	POP H
	POP PSW
	RET
ZERO: 				
	MVI A,00H
	STA 0B04H
	CALL DISPLAY
	CALL DELB
	RET
	
	END